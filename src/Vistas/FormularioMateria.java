    private void JBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBBuscarActionPerformed
        // TODO add your handling code here:
        try{
            Integer codigo=Integer.parseInt(JTCodigo.getText());
//            
              materiaActual=matData.buscarM(codigo);
            if(materiaActual!=null){
                JTNombre.setText(materiaActual.getNombre());
                JREstado.setSelected(materiaActual.isEstado());
                JTAnio.setText((Integer.toString(materiaActual.getAnio())));
                
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"Debe ingresar un nÃºmero vÃ¡lido");
        }
    }//GEN-LAST:event_JBBuscarActionPerformed


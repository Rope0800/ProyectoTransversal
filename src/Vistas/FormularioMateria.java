    private void JBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBGuardarActionPerformed
        // TODO add your handling code here:
        try{
            Integer codigo=Integer.parseInt(JTCodigo.getText());
            String nombre=JTNombre.getText();
            Integer anio=Integer.parseInt(JTAnio.getText());
            if(nombre.isEmpty()){
             JOptionPane.showMessageDialog(this, "No puede haber campos vacÃ­os");
             return;
            }
            
            Boolean estado=JREstado.isSelected();
            if(materiaActual==null){
                materiaActual=new Materias(codigo,nombre,anio,estado);
                matData.guardarMateria(materiaActual);
            }else{
                materiaActual.setIdMateria(codigo);
                materiaActual.setNombre(nombre);
                materiaActual.setAnio(anio);
                
                matData.modificarMateria(materiaActual);
                
            }
            
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"Debe ingresar un CÃ³digo vÃ¡lido");
        }
    }//GEN-LAST:event_JBGuardarActionPerformed

